public class PlayerInfo {
  private String name;
  private Integer score;
  private int x;
  private int y;

  public PlayerInfo() {}

  public PlayerInfo(String name, Integer score, int x, int y) {
    this.name = name;
    this.score = score;
    this.x = x;
    this.y = y;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
